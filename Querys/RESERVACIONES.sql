CREATE TABLE RESERVACIONES (
    ID_RESERVACION      NUMBER(10)      GENERATED BY DEFAULT AS IDENTITY,
    ID_HUESPED          NUMBER(10)      NOT NULL,
    ID_HABITACIONES     NUMBER(10)      NOT NULL,
    FECHA_INGRESO       DATE            NOT NULL,
    FECHA_SALIDA        DATE            NOT NULL,
    ESTADO_RESERVACION  VARCHAR2(20)    NOT NULL,
    ESTADO_REGISTRO     VARCHAR2(10)    NOT NULL,
    -- PRIMARY KEY
    CONSTRAINT RESERVACION_PK
        PRIMARY KEY (ID_RESERVACION),
    -- Fecha salida debe ser posterior a fecha ingreso
    CONSTRAINT CHK_RESERVACION_FECHAS
        CHECK (FECHA_SALIDA > FECHA_INGRESO),
    -- Estado reservación
    -- 1-CONFIRMADA | 2-EN_CURSO | 3-FINALIZADA | 4-CANCELADA
    CONSTRAINT CHK_RESERVACION_ESTADO
        CHECK (ESTADO_RESERVACION IN (
            'CONFIRMADA', 'EN_CURSO', 'FINALIZADA', 'CANCELADA'
        )),
    -- Estado registro
    CONSTRAINT CHK_RESERVACION_ESTADO_REG
        CHECK (ESTADO_REGISTRO IN (
            'ACTIVO', 'ELIMINADO'
        ))
);