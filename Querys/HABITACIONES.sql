CREATE TABLE HABITACIONES (
    ID_HABITACION       NUMBER(10)      GENERATED BY DEFAULT AS IDENTITY,
    NUMERO_HABITACION   NUMBER(5)       NOT NULL,
    TIPO                VARCHAR2(30)    NOT NULL,
    PRECIO              NUMBER(10,2)    NOT NULL,
    CAPACIDAD           NUMBER(3)       NOT NULL,
    ESTADO_HABITACION   VARCHAR2(20)    NOT NULL,
    ESTADO_REGISTRO     VARCHAR2(10)    NOT NULL,

    -- PRIMARY KEY
    CONSTRAINT HABITACION_PK
        PRIMARY KEY (ID_HABITACION),

    -- Número habitación: entero mayor a 0
    CONSTRAINT CHK_HABITACION_NUMERO
        CHECK (NUMERO_HABITACION > 0),


    -- Precio: mayor a 0
    CONSTRAINT CHK_HABITACION_PRECIO
        CHECK (PRECIO > 0),

    -- Capacidad: mínimo 1
    CONSTRAINT CHK_HABITACION_CAPACIDAD
        CHECK (CAPACIDAD >= 1),

    -- Estado habitación: código + descripción
    -- 1-DISPONIBLE | 2-OCUPADA | 3-LIMPIEZA | 4-MANTENIMIENTO
    CONSTRAINT CHK_HABITACION_ESTADO
        CHECK (ESTADO_HABITACION IN (
            'DISPONIBLE', 'OCUPADA', 'LIMPIEZA', 'MANTENIMIENTO'
        )),

    -- Estado registro
    CONSTRAINT CHK_HABITACION_ESTADO_REG
        CHECK (ESTADO_REGISTRO IN (
            'ACTIVO', 'ELIMINADO'
        ))
);