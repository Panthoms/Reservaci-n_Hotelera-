CREATE TABLE HUESPED (
    ID_HUESPED          NUMBER(10)      GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE              VARCHAR2(50)    NOT NULL,
    APELLIDO_PATERNO    VARCHAR2(50)    NOT NULL,
    APELLIDO_MATERNO    VARCHAR2(50)    NOT NULL,
    EMAIL               VARCHAR2(100)   NOT NULL,
    TELEFONO            VARCHAR2(10)    NOT NULL,
    TIPO_DOCUMENTO      VARCHAR2(20)    NOT NULL,
    NUMERO_DOCUMENTO    VARCHAR2(20)    NOT NULL,
    NACIONALIDAD        VARCHAR2(60)    NOT NULL,
    ESTADO_REGISTRO     VARCHAR2(10)    NOT NULL,

    -- PRIMARY KEY
    CONSTRAINT HUESPED_PK
        PRIMARY KEY (ID_HUESPED),

    -- Nombre: 2 a 50 caracteres
    CONSTRAINT CHK_HUESPED_NOMBRE
        CHECK (LENGTH(TRIM(NOMBRE)) BETWEEN 2 AND 50),

    -- Apellido paterno: 2 a 50 caracteres
    CONSTRAINT CHK_HUESPED_AP_PATERNO
        CHECK (LENGTH(TRIM(APELLIDO_PATERNO)) BETWEEN 2 AND 50),

    -- Apellido materno: 2 a 50 caracteres
    CONSTRAINT CHK_HUESPED_AP_MATERNO
        CHECK (LENGTH(TRIM(APELLIDO_MATERNO)) BETWEEN 2 AND 50),

    -- Email: formato válido
    CONSTRAINT CHK_HUESPED_EMAIL
        CHECK (REGEXP_LIKE(EMAIL, '^[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}$')),

    -- Teléfono: exactamente 10 dígitos
    CONSTRAINT CHK_HUESPED_TELEFONO
        CHECK (REGEXP_LIKE(TELEFONO, '^[0-9]{10}$')),


    -- Estado registro: valores permitidos
    CONSTRAINT CHK_HUESPED_ESTADO
        CHECK (ESTADO_REGISTRO IN (
            'ACTIVO', 'ELIMINADO'
        ))
);
